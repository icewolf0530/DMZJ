-(void)getHttpData
{
    
    
    AFHTTPRequestOperationManager *manager =[AFHTTPRequestOperationManager manager];
    manager.requestSerializer =[AFHTTPRequestSerializer serializer];
    manager.responseSerializer =[AFHTTPResponseSerializer serializer];
    [manager GET:@"http://api.dmzj.com/dynamic/comicsearch/(ziduan).json?channel=ios&version=1.1.107" parameters:nil success:^(AFHTTPRequestOperation *operation, id responseObject) {
        sRmodel =[[searchResultModel alloc]mj_setKeyValues:operation.responseString];
        NSLog(@"%@",sRmodel);
    } failure:^(AFHTTPRequestOperation *operation, NSError *error) {
        NSLog(@"%@",error);
    }];
}

